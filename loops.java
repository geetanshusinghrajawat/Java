public class loops {
    public static void main(String[] args) 
    {
        int i=1;
        System.out.println("Table of 10 using while loop:");
        while(i!=11)
        {
            System.out.println(10*i);
            i++;
        }
        System.out.println("Table of 5 using for loop");
        for(int a=1;a!=11;a++)
        {
            System.out.println(5*a);
        }
    }    
}
